
public class Patients {
    int id;
    String name;
    String email;
    String contact;
    String gender;
    int age;
    String community;
    String password;
    
    Patients(int id,String name,String email,String contact,String gender, int age, String community, String password){
        this.id = id;
        this.name = name;
        this.email = email;
        this.contact = contact;
        this.gender = gender;
        this.age = age;
        this.community = community;
        this.password = password;
    }
    
}
